class Sessao:
    def __init__(self, numero:int, corredor:str):
        self.__numero = numero
        self.__corredor = corredor

    def get_corredor(self):
        return self.__numero

    def get_corredor(self):
        return self.__corredor

    def set_numero(self, numero:int):
        self.__numero = numero

    def set_corredor(self, corredor:str):
        self.__corredor = corredor